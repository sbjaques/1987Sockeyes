#!/usr/bin/env node
/**
 * Verify AI-drafted scan descriptions in src/data/media.json against their OCR.
 *
 * For every scan with `needsReview: true`, look up the matching OCR file by
 * imageId, send draft + OCR to Sonnet 4.6, and ask for a structured verdict
 * via tool-use:
 *   - accurate    → flip needsReview=false, keep current text
 *   - needs-fix   → flip needsReview=false, apply suggested corrections
 *   - uncertain   → leave needsReview=true with notes for editor review
 *
 * Resumable: writes one review file per scan to docs/curation/reviews/<id>.json.
 * Re-running the script skips items that already have a review on disk.
 *
 * The applier (apply-scan-reviews.mjs) is a separate step.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import Anthropic from '@anthropic-ai/sdk';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');

// --- env load ---
const envPath = path.join(root, '.env.local');
if (fs.existsSync(envPath)) {
  for (const line of fs.readFileSync(envPath, 'utf-8').split(/\r?\n/)) {
    const m = line.match(/^([A-Z0-9_]+)=(.*)$/);
    if (m && !process.env[m[1]]) process.env[m[1]] = m[2];
  }
}
if (!process.env.ANTHROPIC_API_KEY) {
  console.error('Missing ANTHROPIC_API_KEY. Set in .env.local or env.');
  process.exit(1);
}

const client = new Anthropic();

// --- paths ---
const reviewsDir = path.join(root, 'docs/curation/reviews');
fs.mkdirSync(reviewsDir, { recursive: true });

const ocrDir = path.join(root, 'src/content/private/ocr');
const ocrByImageId = {};
if (fs.existsSync(ocrDir)) {
  for (const f of fs.readdirSync(ocrDir)) {
    const m = f.match(/-i(\d{7,})\.md$/);
    if (m) ocrByImageId[m[1]] = path.join(ocrDir, f);
  }
}

// --- roster context (cached as part of system prompt) ---
const roster = JSON.parse(fs.readFileSync(path.join(root, 'src/data/roster.json'), 'utf-8'));
const rosterNames = roster
  .map(p => {
    const aliases = (p.aliases && p.aliases.length) ? ` (aka ${p.aliases.join(', ')})` : '';
    const role = p.role ? ` [${p.role}]` : '';
    const town = p.hometown ? ` from ${p.hometown}` : '';
    return `- ${p.name}${aliases}${role}${town}`;
  })
  .join('\n');

const systemPrompt = `You are reviewing AI-drafted descriptions of newspaper-clipping scans in a historical archive about the 1987 Richmond Sockeyes, a BC junior-A hockey team that won the Centennial Cup (Canadian junior-A championship) in May 1987 in Humboldt, SK.

Key facts:
- Head coach: Orland Kurtenbach
- Owner: Bruce Taylor
- Captain: Trevor Dickie (#21)
- Assistant captain: Bill Hardy (#16)
- Centennial Cup final: 5-2 win over Humboldt Broncos, May 9 1987
- Centennial Cup MVP: Frank Romeo
- The team's playoff path: Mowat Cup (BCJHL) → Doyle Cup (vs Alberta) → Abbott Cup (Western final) → Centennial Cup (national)

The drafts were generated by Claude Haiku from OCR text. Common failure modes:
1. The OCR file may include adjacent-column noise from non-Sockeyes articles on the same page. The draft sometimes describes the WRONG article — pick the Sockeyes-relevant content if there is any, or flag if the page has nothing Sockeyes-related.
2. AI-drafted headline/byline are often wrong. The OCR is the source of truth — find the actual headline (large bold text) and byline (typically "By [Name]" near the article start).
3. Filename-derived attribution (paper, page number, date, imageId) is reliable — don't change those.

Sockeyes roster — match these names against the OCR to identify Sockeyes-relevant content. Players who later played pro/college are still relevant when the article covers their 1986-87 Sockeyes season or earlier junior career; later-career articles about ex-Sockeyes are also archive-worthy:

${rosterNames}

For each scan, call verify_scan_draft with your verdict:
- "accurate": the draft accurately summarizes the Sockeyes-relevant article on the page. Flip needsReview to false.
- "needs-fix": draft describes the wrong article, has factual errors, or attribution is wrong. Provide corrections.
- "uncertain": OCR is too garbled, the page has no Sockeyes-relevant content, or you cannot tell. Editor will review.

Description constraints (must satisfy these or it fails JSON-schema validation):
- descriptionShort: 20-400 characters, one sentence, factual.
- descriptionLong: 80-2000 characters, 2-4 sentences, factual.
- Both should describe the SOCKEYES-relevant content on the page when present.`;

const verifyTool = {
  name: 'verify_scan_draft',
  description: 'Return a structured verification of an AI-drafted scan description.',
  input_schema: {
    type: 'object',
    properties: {
      verdict: {
        type: 'string',
        enum: ['accurate', 'needs-fix', 'uncertain'],
        description: 'Overall verdict on the draft.'
      },
      sockeyesRelevance: {
        type: 'string',
        enum: ['relevant', 'tangential', 'unrelated'],
        description: 'Whether the OCR contains content directly about the 1987 Sockeyes, a known Sockeyes person, or their opponents/league.'
      },
      issues: {
        type: 'array',
        items: { type: 'string' },
        description: 'Specific factual issues with the draft. Empty array if accurate.'
      },
      correctedDescriptionShort: {
        type: 'string',
        description: '20-400 chars. Only set when verdict = needs-fix. Empty otherwise.'
      },
      correctedDescriptionLong: {
        type: 'string',
        description: '80-2000 chars. Only set when verdict = needs-fix. Empty otherwise.'
      },
      correctedHeadline: {
        type: 'string',
        description: 'Actual headline from OCR. Set when verdict = needs-fix and headline was wrong. Empty if no clear headline in OCR.'
      },
      correctedByline: {
        type: 'string',
        description: 'Actual byline from OCR. Set when verdict = needs-fix and byline was wrong. Empty if no byline.'
      },
      notes: {
        type: 'string',
        description: 'Brief note for the editor. Required when verdict = uncertain. Optional otherwise.'
      }
    },
    required: ['verdict', 'sockeyesRelevance', 'issues', 'correctedDescriptionShort', 'correctedDescriptionLong', 'correctedHeadline', 'correctedByline', 'notes']
  }
};

async function verifyOne(scan) {
  const ocrPath = ocrByImageId[scan.attribution?.imageId];
  if (!ocrPath) {
    return {
      verdict: 'uncertain',
      sockeyesRelevance: 'unrelated',
      issues: ['no matching OCR file found for imageId'],
      correctedDescriptionShort: '',
      correctedDescriptionLong: '',
      correctedHeadline: '',
      correctedByline: '',
      notes: `imageId ${scan.attribution?.imageId} has no OCR file in src/content/private/ocr/.`
    };
  }
  const ocr = fs.readFileSync(ocrPath, 'utf-8').slice(0, 6000);
  const draft = {
    descriptionShort: scan.descriptionShort,
    descriptionLong: scan.descriptionLong,
    headline: scan.attribution?.headline || '',
    byline: scan.attribution?.byline || '',
    paper: scan.attribution?.paper || '',
    page: scan.attribution?.page || '',
    date: scan.date || ''
  };

  const userMsg = `Scan to verify:
- imageId: ${scan.attribution?.imageId}
- date: ${draft.date}
- paper: ${draft.paper} (page ${draft.page})

Current AI-drafted description (verify or correct):
- short: ${draft.descriptionShort}
- long: ${draft.descriptionLong}
- headline: ${draft.headline}
- byline: ${draft.byline}

OCR text (source of truth — may contain adjacent-column noise):
---
${ocr}
---

Call verify_scan_draft with your verdict.`;

  const res = await client.messages.create({
    model: 'claude-sonnet-4-6',
    max_tokens: 2048,
    system: [
      { type: 'text', text: systemPrompt, cache_control: { type: 'ephemeral' } }
    ],
    tools: [verifyTool],
    tool_choice: { type: 'tool', name: 'verify_scan_draft' },
    messages: [{ role: 'user', content: userMsg }]
  });
  const toolUse = res.content.find(b => b.type === 'tool_use');
  if (!toolUse) throw new Error('no tool_use in response');
  return { ...toolUse.input, _usage: res.usage };
}

async function reviewOne(scan) {
  const outPath = path.join(reviewsDir, `${scan.id}.json`);
  if (fs.existsSync(outPath)) return { skipped: 'already-reviewed' };

  const review = await verifyOne(scan);
  const out = {
    id: scan.id,
    imageId: scan.attribution?.imageId,
    date: scan.date,
    paper: scan.attribution?.paper,
    page: scan.attribution?.page,
    review
  };
  fs.writeFileSync(outPath, JSON.stringify(out, null, 2));
  return { wrote: outPath, usage: review._usage };
}

async function pLimit(items, limit, fn) {
  const results = new Array(items.length);
  let next = 0;
  let inFlight = 0;
  return new Promise((resolve, reject) => {
    const launch = () => {
      while (inFlight < limit && next < items.length) {
        const i = next++;
        inFlight++;
        Promise.resolve(fn(items[i], i))
          .then(r => { results[i] = r; })
          .catch(e => { results[i] = { error: e }; })
          .finally(() => {
            inFlight--;
            if (next === items.length && inFlight === 0) resolve(results);
            else launch();
          });
      }
    };
    launch();
  });
}

async function main() {
  const media = JSON.parse(fs.readFileSync(path.join(root, 'src/data/media.json'), 'utf-8'));
  let targets = media.filter(x => x.needsReview === true);
  const max = Number(process.env.MAX_ITEMS || 0);
  if (max > 0) targets = targets.slice(0, max);
  console.log(`needsReview scans:    ${targets.length}${max ? ` (limited from ${media.filter(x => x.needsReview).length} via MAX_ITEMS)` : ''}`);
  console.log(`reviews dir:          ${reviewsDir}`);

  const concurrency = Number(process.env.VERIFY_CONCURRENCY || 8);
  console.log(`concurrency:          ${concurrency}\n`);

  let wrote = 0, skipped = 0, failed = 0;
  let cacheRead = 0, cacheCreate = 0, inputUncached = 0, outputTokens = 0;

  await pLimit(targets, concurrency, async (scan, i) => {
    const prefix = `[${String(i + 1).padStart(3)}/${targets.length}]`;
    try {
      const r = await reviewOne(scan);
      if (r.wrote) {
        wrote++;
        if (r.usage) {
          cacheRead += r.usage.cache_read_input_tokens || 0;
          cacheCreate += r.usage.cache_creation_input_tokens || 0;
          inputUncached += r.usage.input_tokens || 0;
          outputTokens += r.usage.output_tokens || 0;
        }
        process.stdout.write(`${prefix} ✓ ${scan.id}\n`);
      } else {
        skipped++;
      }
    } catch (err) {
      failed++;
      process.stdout.write(`${prefix} ✗ ${scan.id} — ${err.message}\n`);
    }
  });

  console.log('');
  console.log(`Done — wrote: ${wrote}, skipped: ${skipped}, failed: ${failed}`);
  console.log(`Tokens — cache_read: ${cacheRead.toLocaleString()}, cache_create: ${cacheCreate.toLocaleString()}, input_uncached: ${inputUncached.toLocaleString()}, output: ${outputTokens.toLocaleString()}`);
}

main().catch(err => { console.error(err); process.exit(1); });
